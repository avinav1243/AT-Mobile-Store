    private void cbmodelFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_cbmodelFocusLost
try
{
  Class.forName("sun.jdbc.odbc.JdbcOdbcDriver");
  Connection con = DriverManager.getConnection("jdbc:odbc:atmobilestoredsn");
  Statement stmt= con.createStatement();
  String query = "select * from tblstockdetails where ((Brand = '"+cbbrand.getSelectedItem().toString()+"') and (PName = '"+cbmodel.getSelectedItem().toString()+"'))";
  ResultSet rs = stmt.executeQuery(query);
  if(rs.next())
  {
      txtid.setText(rs.getString("ProductID"));
      txtram.setText(rs.getString("RAM"));
      txtrom.setText(rs.getString("ROM"));
      txtslot.setText(rs.getString("MemorySlot"));
      txtfrontcam.setText(rs.getString("FrontCamera"));
      txtrearcam.setText(rs.getString("RearCamera"));
      txtbattery.setText(rs.getString("Battery"));
      txtandver.setText(rs.getString("AndroidVersion"));
      txtratio.setText(rs.getString("ScreentoBody"));
      txtglass.setText(rs.getString("GlassProtection"));
      txtscreen.setText(rs.getString("ScreenResolution"));
      txtport.setText(rs.getString("ChargingPort"));
      txtcharge.setText(rs.getString("QuickCharge"));
      txtprocessor.setText(rs.getString("Processor"));
      txtnetwork.setText(rs.getString("Network"));
      if(rs.getString("WirelessCharging").equals("1"))
      {
          txtwireless.setText("Yes");
      }
      else
      txtwireless.setText("No");
      txtprice.setText(""+rs.getInt("Price"));
      txtqty.setText(""+rs.getInt("Quantity"));
  }
  con.close();
}
catch(Exception e)
{
JOptionPane.showMessageDialog(null, e);
}        // TODO add your handling code here:
    }//GEN-LAST:event_cbmodelFocusLost

