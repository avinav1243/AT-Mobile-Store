    void getmodel()
    {
        try
        {
            Class.forName("sun.jdbc.odbc.JdbcOdbcDriver");
            Connection con = DriverManager.getConnection("jdbc:odbc:atmobilestoredsn");
            Statement stmt= con.createStatement();
            String query = "select PName from tblstockdetails where ((Brand = '"+cbbrand.getSelectedItem().toString()+"') and ((Price >= "+Integer.parseInt(txtminprice.getText())+") and (Price <= "+Integer.parseInt(txtmaxprice.getText())+")))";
            ResultSet rs = stmt.executeQuery(query);
            while(rs.next())
            {
                cbmodel.addItem(rs.getString("PName"));
            }
            con.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }

